    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "select * from StorageFacility where storage_facility_id = '" + sid_tf.getText()+ "'";
            st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rs = st.executeQuery(sql);
            
            if(rs.next()){
                lid_tf.setText(rs.getString(2));
                SimpleDateFormat dbFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                SimpleDateFormat displayFormat = new SimpleDateFormat("dd-MMM-yyyy");
                String sDateStr = rs.getString(3);
                String eDateStr = rs.getString(4);
                Date pDate = dbFormat.parse(sDateStr);
                Date dDate = dbFormat.parse(eDateStr);
                String sFormattedDate = displayFormat.format(pDate);
                String eFormattedDate = displayFormat.format(dDate);
                idate_tf.setText(sFormattedDate);
                odate_tf.setText(eFormattedDate);
                cap_tf.setText(rs.getString(5));
                type_tf.setText(rs.getString(6));
                gas_tf.setText(rs.getString(7));
                w_tf.setText(rs.getString(8));
            }
            else{
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        }
        catch(SQLException ex){
            Logger.getLogger(GasSupply.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Date parsing error: " + ex.getMessage());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

