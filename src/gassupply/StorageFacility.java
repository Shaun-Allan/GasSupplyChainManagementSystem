    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE StorageFacility SET location = ?, incoming_date = ?, outgoing_date = ?, storage_capacity = ?, storage_type = ?, gas_stored = ?, working_gas = ? WHERE storage_facility_id = ?";
            ps = con.prepareStatement(sql);
    
    // Set parameters
            ps.setString(1, lid_tf.getText());
            ps.setString(2, idate_tf.getText());
            ps.setString(3, odate_tf.getText());
            ps.setFloat(4, Float.parseFloat(cap_tf.getText()));
            ps.setString(5, type_tf.getText());
            ps.setString(6, gas_tf.getText());
            ps.setFloat(7, Float.parseFloat(w_tf.getText()));
            ps.setString(8, sid_tf.getText());
    
    // Execute the update
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Successfully Updated");
            } else {
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        } catch (SQLException ex) {
            Logger.getLogger(GasSupply.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid value. Please enter a valid number.");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

