    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "select * from DrillingSite where site_id = '" + siteId_tf.getText()+ "'";
            System.out.println(sql);
            st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rs = st.executeQuery(sql);
            
            if(rs.next()){
                locationId_tf.setText(rs.getString(2));
                SimpleDateFormat dbFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                SimpleDateFormat displayFormat = new SimpleDateFormat("dd-MMM-yyyy");
                String startDateStr = rs.getString(3);
                String endDateStr = rs.getString(4);
                Date startDate = dbFormat.parse(startDateStr);
                Date endDate = dbFormat.parse(endDateStr);
                String startFormattedDate = displayFormat.format(startDate);
                String endFormattedDate = displayFormat.format(endDate);
                start_tf.setText(startFormattedDate);
                end_tf.setText(endFormattedDate);
                empl_tf.setText(rs.getString(5));
                perm_tf.setText(rs.getString(6));
                mud_tf.setText(rs.getString(7));
                bit_tf.setText(rs.getString(8));
                rop_tf.setText(rs.getString(9));
            }
            else{
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        }
        catch(SQLException ex){
            Logger.getLogger(GasSupply.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Date parsing error: " + ex.getMessage());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void insertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertButtonActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "insert into DrillingSite values(?, ?, ?, ?, ?, ?, ?, ?, ?)";
            ps = con.prepareStatement(sql);
            ps.setString(1, siteId_tf.getText());
            ps.setString(2, locationId_tf.getText());
            ps.setString(3, start_tf.getText());
            ps.setString(4, end_tf.getText());
            ps.setInt(5, Integer.parseInt(empl_tf.getText()));
            ps.setFloat(6, Float.parseFloat(perm_tf.getText()));
            ps.setFloat(7, Float.parseFloat(mud_tf.getText()));
            ps.setString(8, bit_tf.getText());
            ps.setFloat(9, Float.parseFloat(rop_tf.getText()));
            ps.execute();
            JOptionPane.showMessageDialog(this, "Succesfully Inserted");
        }
        catch(SQLException ex){
            Logger.getLogger(GasSupply.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_insertButtonActionPerformed

