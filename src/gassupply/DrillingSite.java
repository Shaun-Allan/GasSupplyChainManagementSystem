    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE DrillingSite SET location = ?, operation_start_date = ?, operation_end_date = ?, personnel_employed = ?, permeability = ?, mud_weight = ?, bit_id = ?, rate_of_penetration = ? WHERE site_id = ?";
            ps = con.prepareStatement(sql);
    
    // Set parameters
            ps.setString(1, locationId_tf.getText());
            ps.setString(2, start_tf.getText());
            ps.setString(3, end_tf.getText());
            ps.setString(4, empl_tf.getText());
            ps.setString(5, perm_tf.getText());
            ps.setString(6, mud_tf.getText());
            ps.setString(7, bit_tf.getText());
            ps.setString(8, rop_tf.getText());
            ps.setString(9, siteId_tf.getText());
    
    // Execute the update
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Successfully Updated");
            } else {
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        } catch (SQLException ex) {
            Logger.getLogger(GasSupply.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid basic salary value. Please enter a valid number.");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

