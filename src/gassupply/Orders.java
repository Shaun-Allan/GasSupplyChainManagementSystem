    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE Orders SET customer_id = ?, date_placed = ?, due_date = ?, quantity = ?, status = ?, gas_id = ?, amt_paid = ? WHERE order_id = ?";
            ps = con.prepareStatement(sql);
    
    // Set parameters
            ps.setString(1, eid_tf.getText());
            ps.setString(2, pdate_tf.getText());
            ps.setString(3, ddate_tf.getText());
            ps.setInt(4, Integer.parseInt(qty_tf.getText()));
            ps.setString(5, status_tf.getText());
            ps.setString(6, gas_tf.getText());
            ps.setFloat(7, Float.parseFloat(amt_tf.getText()));
            ps.setString(8, oid_tf.getText());
    
    // Execute the update
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Successfully Updated");
            } else {
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        } catch (SQLException ex) {
            Logger.getLogger(GasSupply.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid basic salary value. Please enter a valid number.");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

