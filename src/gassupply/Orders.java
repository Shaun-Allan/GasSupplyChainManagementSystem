            String sql = "insert into Orders values(?, ?, ?, ?, ?, ?, ?, ?)";
            ps = con.prepareStatement(sql);
            ps.setString(1, oid_tf.getText());
            ps.setString(2, eid_tf.getText());
            ps.setString(3, pdate_tf.getText());
