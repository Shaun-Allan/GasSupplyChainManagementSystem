    private void searchButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButton4ActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "select * from Market where market_id = '" + mid_tf.getText()+ "'";
            st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rs = st.executeQuery(sql);
            
            if(rs.next()){
                name_tf.setText(rs.getString(2));
                lid_tf.setText(rs.getString(3));
                con_tf.setText(rs.getString(4));
                capacity_tf.setText(rs.getString(5));
                curlevel_tf.setText(rs.getString(6));
                refil_tf.setText(rs.getString(7));
                SimpleDateFormat dbFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                SimpleDateFormat displayFormat = new SimpleDateFormat("dd-MMM-yyyy");
                String orderDateStr = rs.getString(8);
                String refilDateStr = rs.getString(7);
                Date orderDate = dbFormat.parse(orderDateStr);
                Date refilDate = dbFormat.parse(refilDateStr);
                String orderFormattedDate = displayFormat.format(orderDate);
                String refilFormattedDate = displayFormat.format(refilDate);
                refil_tf.setText(refilFormattedDate);
                lorder_tf.setText(orderFormattedDate);
                manager_tf.setText(rs.getString(9));
            }
            else{
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        }
        catch(SQLException ex){
            Logger.getLogger(GasSupply.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Date parsing error: " + ex.getMessage());
        }
    }//GEN-LAST:event_searchButton4ActionPerformed

