    private void updateButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButton4ActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE Market SET name = ?, location = ?, contact_number = ?, capacity = ?, current_level = ?, last_refill_date = ?, last_order_date = ?, manager_id = ? WHERE market_id = ?";
            ps = con.prepareStatement(sql);
    
    // Set parameters
            ps.setString(1, name_tf.getText());
            ps.setString(2, lid_tf.getText());
            ps.setInt(3, Integer.parseInt(con_tf.getText()));
            ps.setFloat(4, Float.parseFloat(capacity_tf.getText()));
            ps.setFloat(5, Float.parseFloat(curlevel_tf.getText()));
            ps.setString(6, refil_tf.getText());
            ps.setString(7, lorder_tf.getText());
            ps.setString(8, manager_tf.getText());
            ps.setString(9, mid_tf.getText());
    
    // Execute the update
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Successfully Updated");
            } else {
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        } catch (SQLException ex) {
            Logger.getLogger(GasSupply.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid basic salary value. Please enter a valid number.");
        }
    }//GEN-LAST:event_updateButton4ActionPerformed

